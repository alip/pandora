#include "pandora-defs.h"

#include <sys/types.h>
#include <signal.h>
#include <time.h>

#include <pinktrace/easy/pink.h>

inline
int
pkill(pid_t pid)
{
	return pandora->config->core.kill.ptrace ? pink_trace_kill(pid) : kill(pid, SIGKILL);
}
